public class Variables {
    public static void main(String[] args) {
        int i = 50;
        short s = 10;
        float f = 10.5f;
        char c = 'A';
        byte b = 12;
        
        System.out.println(i);
        System.out.println(s);
        System.out.println(f);
        System.out.println(c);
        System.out.println(b);
    }
}
