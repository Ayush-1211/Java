package operators.and.expressions;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 14, b = 5;
        float h = 14.3f, j = 3.2f;
        
        int c = a / b;
        int r = a % b;
        float x = (float)a / b;
        float z = h % j;
        
        System.out.println(c);
        System.out.println(r);
        System.out.println(x);
        System.out.println(z);
    }
}
