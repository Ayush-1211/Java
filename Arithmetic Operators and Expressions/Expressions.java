package operators.and.expressions;

public class Expressions {
    public static void main(String[] args) {
        byte a = 10;
        short b = 15;
        float c = 12.5f;
        long z = 123l;
        int n = 3;
        double d = 45.5;
        char g = 40;
        
        int x = a + b;
        
        float v = c * z;
        float m = n * c;
        double o = d * c;
        
        int y = g - n;
        
        System.out.println(x);
        System.out.println(v);
        System.out.println(m);
        System.out.println(o);
        System.out.println(y);
    }
}
