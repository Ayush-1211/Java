package conditional;
public class Relational_Operators {
    public static void main(String[] args) {
        int a=5, b=10, c=15, d=5;
        
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a==d);
        System.out.println(a>=d);
        System.out.println(b<=a);
    }
}
